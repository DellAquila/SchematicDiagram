  for _ , key in ipairs(keys) do
    local part = {}
    dashkey = key[1]:gsub("_", "-")
    for _ , elem in ipairs(elems) do
      part[elem[1]] =
      {
        filename = string.format("__SchematicDiagram__/graphics/entity/%s/%s-%s-%s.png", dashkey, dashkey, key[2], elem[2]),
